package configuration.domain;

public class GroupPoolConfig {

    /**
     * 任务组别ID
     */
    private int groupId;

    /**
     * 数量
     */
    private int count;

    public int getGroupId() {
        return groupId;
    }

    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

}
